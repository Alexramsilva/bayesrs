import streamlit as st
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

st.title("Posterior de Bayes al estilo histórico")

st.write("""
Esta app calcula la distribución posterior de \(x\) usando discretización,
como Bayes lo hizo en su ensayo de 1763.
""")

# Entradas de usuario
p = st.number_input("Número de éxitos (p)", min_value=0, value=7, step=1)
q = st.number_input("Número de fracasos (q)", min_value=0, value=3, step=1)
intervalos = st.number_input("Número de intervalos para discretización", min_value=2, value=10, step=1)

# Discretización de x
x_values = np.linspace(0,1,intervalos+1)
f_values = x_values**p * (1 - x_values)**q

# Normalización
probabilidades = f_values / np.sum(f_values)

# Crear tabla de resultados
tabla = pd.DataFrame({
    "x": x_values.round(2),
    "x^p*(1-x)^q": f_values.round(6),
    "Probabilidad aprox.": probabilidades.round(4)
})

st.subheader("Tabla de probabilidades aproximadas por intervalos de x")
st.dataframe(tabla)

# Gráfica
fig, ax = plt.subplots()
ax.bar(x_values, probabilidades, width=0.05, color='skyblue', edgecolor='black')
ax.set_xlabel("x")
ax.set_ylabel("Probabilidad aproximada")
ax.set_title(f"Posterior de Bayes (discretizado en {intervalos} intervalos)")
st.pyplot(fig)

# Probabilidad para un valor específico
x_val = st.slider("Selecciona x para ver probabilidad aproximada", 0.0, 1.0, 0.7, 0.01)
# Encontrar el intervalo más cercano
idx = (np.abs(x_values - x_val)).argmin()
st.write(f"Probabilidad aproximada en x ≈ {x_values[idx]:.2f}: {probabilidades[idx]:.4f}")